print(type(15)) # int -> integer -> número entero
print(type(15.7)) # float -> número real
print(type(18j)) # complex -> número complejo
print(type("Hola 15")) # str -> string -> texto
print(type([15,"hola",20.5])) # list -> lista -> conjunto ordenado y mutable de datos
print(type((15,"hola",20.5))) # tuple -> tupla -> conjunto ordenado e inmutable de datos
print(type({"hola": 15, 2: [1,2]})) # dict -> diccionario -> conjunto ordenado y nombrado de datos
print(type(True)) # bool -> booleano -> Verdadero/Falso
print(type(False))